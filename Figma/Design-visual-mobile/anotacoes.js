// 10 heurísticas de Nielsen:
// Jakob Nielsen é um cientista da computação que estudou bastante sobre usuários e usabilidade, ele é um dos precursores dessa área.
// - 1) VIsibilidade do status do sistema (Onde o usuário se encontra);
// - 2) Compatibilidade do sistema e o mundo real (utilizar uma linguagem padrão, como ícones que é uma linguagem universal, botões).
// - 3) Controle e liberdade do usuário (Dar opções de saída caso aconteça algo que usuário não queira).
// - 4) Consistência e padronização (manter uma escolha de design, como tamanho e peso, cores, família tipográfica)
// - 5) Prevenção de error
// - 6) Reconhecimento, ao invés de memorização.
// - 7) Eficiência e flexibilidade de uso.( O usuário leigo precisa de mais informações para executar ações conforme vai evoluindo no uso do sistema, podendo ser apresentado a atalhos que facilitem as mesmas ações.)
// - 8) Estética e design minimalista.
// - 9) Reconhecimento de error (mostrar quando o usuário comete error, pq, como resolver)
// - 10) Ajuda e documentação.

