// O que são diretrizes do design de interface?
// São boas práticas para conseguir criar interfaces apuradas e bem feitas.
// Algumas diretrizes:
// - Simplicidades: tornar mais claro para o usuário o objetivo da plataforma. (máx 3 familias tipográficas, máximo 5 cores, usar espaço em branco para trazer suavidade, por exemplo.)
// - Hierarquia: mostra o que o usuário irá utilizar primeiro (tamanho da fonte, peso, tamanho da imagem ou cores).
// - Navegação facilitada.
// - Padrão visual: ajuda o usuário não se perder na plataforma.
// - Responsividade.

//Design Patterns:
// Estratégias visuais para resolver problemas comuns à interfaces.

//Design atômico:
// Metodologia para criar sistemas de design modulares, leva em consideração que existem elementos no design de interface que soltos, não fazem sentido existir, porém, juntos, começam a criar um todo organizado.
// - Átomos: elementos que sozinhos não fazem sentido (label, botão, input);
// - Moléculas: agrupamento dos átomos que juntos começam a fazer sentido (label + input + botão).
// - Organismo: agrupamento de moléculas (menu,header, footer);
// - Templates: vários organimos juntos (menu + header + footer + imagem);
// - Telas: conteúdos reais como produto final.

// Construção de Grids:
// Grids é como se fossem uma malha onde consigo posicionar os elementos, ajudando a posisionar e ter um padrão de posicionamento.

// Escalas Modulares:
// Os tamanhos dos textos devem ter harmonia.
// Uma escala modular é uma sequência de números que se relacionam uns com os outros de maneira significativa. 
