// O react foi criado em 2013 pelo Facebook, para solucionar problemas que o Angular.Js não solucionava na época.
// Componentes e gerenciamento de estado.

// Com o tempo, o mobile se tornou fundamental. Logo, o React começou a sentir a pensar em criar um novo pacote para Mobile.
// O React Native surgiu em 2015.
// Ele é muito similar ao React.Js, as vezes não dá nem pra saber qual é qual.
// Uma tecnologia híbrida para desenvolver app para Android e IOS


// Como começar?
// Primeiro instalar o node.Js (https://nodejs.org/en/), que já vem instalado o NPM (gerenciador de pacotes e facilita o download de bibliotecas de código em geral para ajudar no desenvolvimento)
// - Instalar a ferramento Expo po meio do comando: --> npm install expo-cli --global

// O que é Expo
// Expo é uma framework, um conjunto de ferramentas e serviços construídos em torno do React Native que ajudam a construir, iterear rapidamente em aplicativos iOS, Android e web a partir da mesma base de código JS/TS.
// O Expo abstrai toda a configuração inicial que é preciso para começar a programar em React Native, tornando mais simple o uso de mapas, câmera e sensores. 
// Expo é um pacote node.
// Os aplicativos criados com o Expo em geral ocupam um pouco mais de espaço no celular do que aplicativos criados da forma tradicional em React Native, pois o expo já mantém todos os recursos necessários em caso de atualizações OTA.

// Para criar o projeto React Native 
// --> npm install -g expo-cli  // Para criar
// --> expo init <nome-projeto>
// --> cd <nome-projeto>
// --> npm start      // Para startar o servidor